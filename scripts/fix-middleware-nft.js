/**
 * Workaround for Next.js 16 Turbopack middleware + NFT bug.
 * Turbopack compiles middleware as Edge runtime chunks instead of middleware.js.
 * Vercel deployment expects both middleware.js and middleware.js.nft.json.
 * See: https://github.com/vercel/next.js/discussions/91609
 */
const fs = require('fs');
const path = require('path');

const nextDir = path.join(process.cwd(), '.next');
const serverDir = path.join(nextDir, 'server');
const middlewareJs = path.join(serverDir, 'middleware.js');
const nftFile = path.join(serverDir, 'middleware.js.nft.json');

// Read the middleware manifest to get the actual edge chunks
const manifestPath = path.join(serverDir, 'middleware-manifest.json');
let edgeFiles = [];

if (fs.existsSync(manifestPath)) {
  try {
    const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
    const middlewareEntry = manifest.middleware?.['/'] || manifest.functions?.['/'];
    if (middlewareEntry?.files) {
      edgeFiles = middlewareEntry.files;
    }
  } catch (e) {
    console.warn('Could not read middleware manifest:', e.message);
  }
}

// Create a minimal middleware.js that references the edge chunks
// This allows Vercel to trace the middleware correctly
// Edge files in manifest are like "server/edge/chunks/..." but middleware.js is in .next/server/
// So we need to strip the "server/" prefix for correct relative paths
const middlewareContent = `// Auto-generated by fix-middleware-nft.js
// Next.js 16 Turbopack compiles middleware as Edge runtime chunks
// This file serves as an entry point for Vercel deployment
${edgeFiles.map(f => {
  const relativePath = f.replace(/^server\//, '');
  return `require('./${relativePath}');`;
}).join('\n')}
module.exports = {};
`;

// Create NFT JSON with the actual edge chunk files
// Manifest paths are "server/edge/chunks/..." relative to .next/
// NFT paths are relative to .next/server/, so strip "server/" prefix
const nftContent = JSON.stringify({
  version: 1,
  files: edgeFiles.map(f => f.replace(/^server\//, ''))
}, null, 2);

// Ensure server directory exists
try {
  fs.mkdirSync(serverDir, { recursive: true });
} catch (e) {
  // directory may already exist
}

// Create missing middleware.js entry point using atomic write
try {
  fs.writeFileSync(middlewareJs, middlewareContent, { flag: 'wx' });
  console.log('Created middleware.js successfully');
} catch (e) {
  if (e.code === 'EEXIST') {
    console.log('middleware.js already exists');
  } else {
    throw e;
  }
}

// Create missing NFT (Node File Trace) JSON using atomic write
try {
  fs.writeFileSync(nftFile, nftContent, { flag: 'wx' });
  console.log('Created middleware.js.nft.json successfully');
} catch (e) {
  if (e.code === 'EEXIST') {
    console.log('middleware.js.nft.json already exists');
  } else {
    throw e;
  }
}
